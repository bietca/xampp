package model.bean.admin;

public class TheLoaiNgayDang {
	private String date;
	private int toanThoiGian;
	private int banThoiGian;
	private int thucTap;
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getToanThoiGian() {
		return toanThoiGian;
	}
	public void setToanThoiGian(int toanThoiGian) {
		this.toanThoiGian = toanThoiGian;
	}
	public int getBanThoiGian() {
		return banThoiGian;
	}
	public void setBanThoiGian(int banThoiGian) {
		this.banThoiGian = banThoiGian;
	}
	public int getThucTap() {
		return thucTap;
	}
	public void setThucTap(int thucTap) {
		this.thucTap = thucTap;
	}
	
	
}
